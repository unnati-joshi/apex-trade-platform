  return (
    <div className="flex min-h-screen items-center justify-center bg-background px-4 bg-grid">
      <div className="max-w-md text-center panel p-10">
        <div className="numeric text-6xl font-semibold tracking-tight text-foreground">404</div>
        <h2 className="mt-3 text-lg font-semibold">Page not found</h2>
        <p className="mt-2 text-sm text-muted-foreground">
          The route you're looking for doesn't exist or has been moved.
        </p>
        <div className="mt-6">
          <Link
            to="/"
            className="inline-flex items-center justify-center rounded-md bg-primary px-4 py-2 text-sm font-medium text-primary-foreground transition-colors hover:opacity-90"
          >
            Return home
          </Link>
        </div>
      </div>
    </div>
  );
